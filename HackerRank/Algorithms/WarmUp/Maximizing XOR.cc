int maxXor(int l, int r) {
    int res = 0;
    int i = 0;
    while (r > l) {
        int u = (r - l >= 1)? 1: 0;
        res |= (u << i);
        r >>= 1;
        l >>= 1;
        i++;
    }
    return res;
}

